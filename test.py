# 2. 定义调色板 (和 C++ 里的 catColors 对应)
palette = {
    0: (0, 0, 0),          # 背景: 黑色
    1: (255, 158, 205),    # 猫: 粉色
    2: (30, 30, 50),       # 眼睛/ZZZ: 深色
    3: (30, 144, 255),      # 被子: 蓝色
    4: (255, 255, 255),
    5: (125, 79, 102)
}

def generate_image():
    # 创建 32x32 的 RGB 图像
    img = Image.new('RGB', (32, 32))
    pixels = img.load()

    # 填充像素
    for y in range(32):
        for x in range(32):
            val = cat_sleeping_data[y * 32 + x]
            pixels[x, y] = palette.get(val, (0,0,0))

    # 因为 32x32 太小了，放大了看才清楚 (放大 10 倍，不平滑处理保持像素感)
    img_large = img.resize((320, 320), Image.NEAREST)
    
    img_large.save("real_cat_sleeping.png")
    print("生成成功！请查看当前目录下的 real_cat_sleeping.png")
    img_large.show()

if __name__ == "__main__":
    generate_image()